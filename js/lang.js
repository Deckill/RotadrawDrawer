let currentLang = localStorage.getItem('appLang');
if (!currentLang) {
  const browserLang = navigator.language.slice(0, 2);
  currentLang = ['ko', 'en', 'ja', 'zh'].includes(browserLang) ? browserLang : 'en';
}

function setLanguage(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  localStorage.setItem('appLang', lang);
  applyTranslations();
  
  if(typeof render === 'function') render();
  if(typeof refreshGroupList === 'function') refreshGroupList();
  if(typeof updatePropsPanel === 'function') updatePropsPanel();
  
  const m = window.currentMode || 'canvas';
  const sbMode = document.getElementById('sb-mode');
  if(sbMode) {
    const modes = { 'canvas': '캔버스', 'draw': '그리기', 'arrange': '배치', 'label': '레이블' };
    sbMode.innerText = t("모드: ") + t(modes[m]);
  }
}

function t(key) {
  const dict = translations[currentLang] || translations['en'];
  return dict[key] !== undefined ? dict[key] : key;
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if(key) {
      if(el.tagName === 'INPUT' && el.type === 'button') {
        el.value = t(key);
      } else {
        el.innerHTML = t(key);
      }
    }
  });
}

document.addEventListener('DOMContentLoaded', () => {
  applyTranslations();
});
